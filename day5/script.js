function solution(seeds, map) {
    let min = Infinity;
    seeds.forEach(seed => min = Math.min(min, findLocation(seed, map)));
    return min;
}

function findLocation(seed, map) {
    let location = seed;
    map.forEach(table => {
        for (let line of table) {
            const [val, start, length] = line;
            if (location >= start && location < start + length) {
                location = val + location - start;
                break;
            }
        }
    });
    return location;
}

